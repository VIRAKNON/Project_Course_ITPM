<script>
   $(document).ready(function () {
    // Function to update total price and total number of items
    function updateTotalPrice() {
        var totalPrice = 0;
        var totalItems = 0;
        $(".price_item").each(function (index, element) {
            var price = parseFloat($(element).text().replace("$", ""));
            var quantity = parseInt($(element).closest(".row").find(".quantity-value").text());
            totalPrice += price * quantity;
            totalItems += quantity;
        });
        $(".price_total").text("$" + totalPrice.toFixed(2));
        $(".number_item").text(totalItems); // Update total number of items
    }

    // Quantity Increment
    $(".quantity .btn-light:last-child").on("click", function () {
        var quantityValue = $(this).siblings(".quantity-value");
        var currentValue = parseInt(quantityValue.text());
        quantityValue.text(currentValue + 1);
        updateTotalPrice();
    });

    // Quantity Decrement
    $(".quantity .btn-light:first-child").on("click", function () {
        var quantityValue = $(this).siblings(".quantity-value");
        var currentValue = parseInt(quantityValue.text());
        if (currentValue > 1) {
            quantityValue.text(currentValue - 1);
            updateTotalPrice();
        }
    });

    // Delete Action
    $(".delete").on("click", function () {
        // Add your delete logic here
        $(this).closest(".row").remove(); // Remove the entire row when an item is deleted
        updateTotalPrice();
        alert("Item deleted!");
    });

    // Initial total price calculation
    updateTotalPrice();
});

</script>
